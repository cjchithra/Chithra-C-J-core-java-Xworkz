class IndianRupee{
	private int rupeeId;
	private String rupeeName;
	private String printDate;
	private String color;
	private int serialNo;
	private int noOfLanguages;
	private String backSidePriceName;
	private String location;
	
	public void setRupeeId(int rupeeId){
		this.rupeeId = rupeeId;
	}
	public int getRupeeId(){
		return rupeeId;
	}
	
	public void setRupeeName(String rupeeName){
		this.rupeeName = rupeeName;
	}
	public String getRupeeName(){
		return rupeeName;
	}
	
	public void setPrintDate(String printDate){
		this.printDate = printDate;
	}
	public String getPrintDate(){
		return printDate;
	}
	
	public void setColor(String color){
		this.color = color;
	}
	public String getColor(){
		return color;
	}
	
	public void setSerialNo(int serialNo){
		this.serialNo = serialNo;
	}
	public int getSerialNo(){
		return serialNo;
	}
	
	public void setNoOfLanguages(int noOfLanguages){
		this.noOfLanguages = noOfLanguages;
	}
	public int getNoOfLanguages(){
		return noOfLanguages;
	}
	
	public void setBackSidePriceName(String backSidePriceName){
		this.backSidePriceName = backSidePriceName;
	}
	public String getBackSidePriceName(){
		return backSidePriceName;
	}
	
	public void setLocation(String location){
		this.location = location;
	}
	public String getLocation(){
		return location;
	}
	
}